    WriteReadUserProfile writeReadUserProfile = new WriteReadUserProfile();
    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        File file = new File(jTextField1.getText() + ".txt");
        if (jTextField1.getText().equals("Please enter your name")){
            jTextArea2.setText("Please enter your name");
        }else{
            if(!file.exists()){
        try {
                jTextField1.setEditable(false);
                value = true;
                writeReadUserProfile.write(jTextField1.getText() + ".txt", "Your operations: \n");
                jTextArea2.setText(writeReadUserProfile.read(jTextField1.getText() + ".txt"));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Calculator.class.getName()).log(Level.SEVERE, null, ex);
            }
            }else{
            try {
                jTextField1.setEditable(false);
                value = true;
                jTextArea2.setText(writeReadUserProfile.read(jTextField1.getText() + ".txt"));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Calculator.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        }
    }//GEN-LAST:event_jButton23ActionPerformed

