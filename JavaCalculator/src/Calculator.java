    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        String text = jTextField3.getText();
        if (!text.equals("")){
            StringBuilder stringBuilder = new StringBuilder();
            stringBuilder.append(text);
            if (stringBuilder.length() == 1){
                jTextField3.setText("0");
            }else{
                stringBuilder.deleteCharAt(text.length() - 1);
                jTextField3.setText(stringBuilder.toString());
            }
        }else{
            jTextField3.setText("0");
        }
    }//GEN-LAST:event_jButton16ActionPerformed

    Compute compute = new Compute();
    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed
        try {
            String operations = null;
            if (value){
                operations = jTextField3.getText();
            }else{
                jTextArea2.setText("Profile off");
            }
            jTextField3.setText(Double.toString(compute.parse(jTextField3.getText())));
            if (value){
                writeReadUserProfile.update(jTextField1.getText() + ".txt", operations + " = " +jTextField3.getText() + " ;");
                jTextArea2.setText(writeReadUserProfile.read(jTextField1.getText() + ".txt"));
            }else{
                jTextArea2.setText("Profile off");
            }
        } catch (Exception ex) {
            jTextField3.setText("Error: invalid input");
        }
    }//GEN-LAST:event_jButton21ActionPerformed

