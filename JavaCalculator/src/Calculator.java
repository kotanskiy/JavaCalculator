    private void jButton31ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton31ActionPerformed
        if (value){
            try {
                writeReadUserProfile.delete(jTextField1.getText() + ".txt");
                value = false;
                jTextArea2.setText("Profile( " + jTextField1.getText() + " )is deleted");
                jTextField1.setEditable(true);
                jTextField1.setText("Please enter your name");
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Calculator.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            jTextArea2.setText("Please, enter your name for deleting!");
        }
    }//GEN-LAST:event_jButton31ActionPerformed
    
